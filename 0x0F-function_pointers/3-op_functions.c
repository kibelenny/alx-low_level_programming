#include "3-calc.h"

/**
 * op_add - Returns the sum of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub of two numbers
 * @a: first number
 * @b: second number
 * Return: sub of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns mul of 2 numbers
 * @a: first numbers
 * @b: second number
 * Return: mul of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return div of 2 numbers
 * @a: first number
 * @b: second number
 * Return: Returns div of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Returns the modulus two numbers
 * @a: first number
 * @b: second number
 * Return: modof a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
